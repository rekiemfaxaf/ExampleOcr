public class PokeData {

    private String pokeName;
    private Integer pokeCp;
    private Integer pokeHp;
    private Integer pokeDust;

    public String getPokeName() {
        return pokeName;
    }

    public void setPokeName(String pokeName) {
        this.pokeName = pokeName;
    }

    public Integer getPokeCp() {
        return pokeCp;
    }

    public void setPokeCp(Integer pokeCp) {
        this.pokeCp = pokeCp;
    }

    public Integer getPokeHp() {
        return pokeHp;
    }

    public void setPokeHp(Integer pokeHp) {
        this.pokeHp = pokeHp;
    }

    public Integer getPokeDust() {
        return pokeDust;
    }

    public void setPokeDust(Integer pokeDust) {
        this.pokeDust = pokeDust;
    }
}
